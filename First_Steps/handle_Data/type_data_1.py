
num_int = 5
num_float = 7.2
str_data = 'I am str'

print("")
print('Concat obj type inti')

print('Numero:', num_int)# concatenacao padrao
print('Numero: %i'  %num_int)# %i sera substituido por um numero inteiro
print('Numero: ' + str(num_int))# tranforma em uma string




print("")
print('Concat obj type float')

print('Concat with:', num_float)# concatenacao padrao

print('Concat with: %.2f' %num_float)
''' %.2f sera substituido por um numero decimal onde 2 sera
a quantidade de casas decimais depois do ponto '''

print('Concat with:' + str(num_float))# tranforma em uma string





print("")
print('Concat obj type string')

print('Concat with:', str_data)
print('Concat with: %s' %str_data)
print('Concat with:' + str_data)










