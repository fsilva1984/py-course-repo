

name = 'Flavio F. Silva'

# pegando uma caractere pelo seu indice na cadeia.

print(f'{name[0]} {name[1]} {name[2]}')

print('')
# pegando caracteres do final da string

print(name[-5])

print('')
# pegando uma cadeia de caracteres do indice 0 ate o 9

print(name[:9])

print('')
# pegando uma cadeia de caracteres do indice 9 ate o final da string

print(name[9:])


print('')
# pegando uma cadeia de caracteres passando o indice inicial e o final

print(name[7:15])


print('')
# pegando uma cadeia de caracteres passando o indice inicial o final e
# de quantos em quantos passos se dara.

print(name[0:15:5])


print('')
# pegando toda a cadeia de caracteres
 
print(name[:])


print('')
# espelhando toda a cadeia de caracteres

print(name[:: -1])