
# CONVERTENDO INT PARA FLOAT

number_int = 24
converte_float = float(number_int)
print(converte_float)

# na divisao de um numero ele e automaticamente convertido para float

divide_num = number_int / 2
print(divide_num)

# CONVERTENDO DE INT PARA FLOAT

integer_num = int(converte_float)
print(integer_num)

# DIVISAO PRESERVANDO UM NUMERO INTEIRO USA-SE //

inteiro_dividido = 10 // 2
print(inteiro_dividido)

# NUMERO PARA STRING

string_num = str(integer_num)
print(type(string_num))


